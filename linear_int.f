c-----------------------------------------------------------------------
c-----------------------------------------------------------------------
      Subroutine linear_int(x1, x2, y1, y2, x, y)
      Implicit None
c-----------------------------------------------------------------------
c-----------------------------------------------------------------------
c input:
      real*8 x1, x2, y1, y2, x
c output:
      real*8 y

c begin:
      y = y1 + (y2 - y1)/(x2 - x1) * (x - x1)
      
      return
      end

